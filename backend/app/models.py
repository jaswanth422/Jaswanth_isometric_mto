"""
Pydantic models for the Material Take-Off (MTO) data structure.

These are the single source of truth for the shape of data flowing through
the pipeline (Extractor -> Reader -> Verifier -> Normalizer) and out to the
frontend. Every stage of the pipeline validates against these models, so a
malformed LLM/OCR output fails loudly here rather than silently corrupting
the response.
"""
from __future__ import annotations

from typing import Literal, Optional
from pydantic import BaseModel, ConfigDict, Field

Category = Literal[
    "PIPE",
    "FITTING",
    "FLANGE",
    "VALVE",
    "GASKET",
    "BOLT",
    "SUPPORT",
    "INSTRUMENTATION",
]
Unit = Literal["M", "EA", "NO", "SET"]
VerificationStatus = Literal["match", "conflict", "unverified", "no_bom_available"]


